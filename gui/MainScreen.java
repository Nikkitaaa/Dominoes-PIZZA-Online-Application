    private void btnOrderHistroyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnOrderHistroyMouseClicked
        cl.show(cardPanel, "cardLedger");
        List<Ledger> data = new ArrayList<>();
        try {
            data = LedgerDAO.getAllRecords();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        DefaultTableModel model = (DefaultTableModel) tableLedger.getModel();
        for (int i = model.getRowCount() - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        Object[] row = new Object[6];
        for (Ledger ledger : data) {
            row[0] = ledger.getId();
            row[1] = ledger.getName();
            row[2] = ledger.getEmail();
            row[3] = ledger.getAmount();
            Date d = ledger.getSchedule();
            SimpleDateFormat sl = new SimpleDateFormat("dd-MM-yyyy");
            row[4] = sl.format(d);
            SimpleDateFormat sl2 = new SimpleDateFormat("HH:mm:ss");
            row[5] = sl2.format(d);
            model.addRow(row);
        }

    }//GEN-LAST:event_btnOrderHistroyMouseClicked

